    public function save(User $user): void
    {
        $this->getEntityManager()->persist($user);
        $this->getEntityManager()->flush();
    }

    public function getById(Uuid $id): ?User
    {
        $user = $this->find($id);
        if (null === $user) {
            throw new \RuntimeException('User not found');
        }

        return $user;
    }
